let text1 = "HELLO WORLD";
let text2 = "Hello World!";

//toLowerCase
// The toLowerCase() method converts a string to lowercase letters 
// and it does not change the orginal string.

let resultString1 = text1.toLowerCase();
console.log(resultString1);



//toUpperCase
//The toUpperCase() method converts a string to uppercase letters.
//it does not change the original string.
let resultString2 = text2.toUpperCase();
console.log(resultString2);


//repeat
//Returns string with number of copies
//it does not change original string
//returns a new string
let text3="Hello";
let resultString3 = text3.repeat(2);
console.log(resultString3);


